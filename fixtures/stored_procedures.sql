CREATE OR REPLACE PROCEDURE CREATE_ODS_FARM_ID_TIMESTAMP()
LANGUAGE SQL
AS $$
DROP TABLE IF EXISTS ODS_FARM_ID_TIMESTAMP;
CREATE TABLE IF NOT EXISTS ODS_FARM_ID_TIMESTAMP (
    ID SERIAL PRIMARY KEY,
    COMPRESSION VARCHAR(40) NOT NULL,
    IMAGE_BLOB BYTEA NOT NULL,
    CHANNEL SMALLINT NOT NULL,
    TIMESTAMP TIMESTAMP NOT NULL,
    ANNOTATIONS JSON NOT NULL
);
$$;